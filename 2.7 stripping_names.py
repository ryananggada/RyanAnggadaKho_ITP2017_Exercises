name = "\tRyan Anggada Kho\n"
lstrip_name = name.lstrip()
rstrip_name = name.rstrip()
strip_name = name.strip()
print("This is the initial name: {}".format(name))
print("This is a name stripped to the left side: {}".format(lstrip_name))
print("This is a name stripped to the right side: {}".format(rstrip_name))
print("This is a name fully stripped: {}".format(strip_name))
